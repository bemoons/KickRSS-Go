// OpenAI Chat Completion Structs
type ChatMessage struct {
	Role    string `json:"role"`
	Content string `json:"content"`
}

type ChatCompletionRequest struct {
	Model          string        `json:"model"`
	Messages       []ChatMessage `json:"messages"`
	ResponseFormat interface{}   `json:"response_format,omitempty"`
	MaxTokens      *int          `json:"max_tokens,omitempty"`
	Stream         bool          `json:"stream,omitempty"`
}

type ChatCompletionResponse struct {
	Choices []struct {
		Message struct {
			Content string `json:"content"`
		} `json:"message"`
	} `json:"choices"`
	Usage struct {
		PromptTokens     int `json:"prompt_tokens"`
		CompletionTokens int `json:"completion_tokens"`
		TotalTokens      int `json:"total_tokens"`
	} `json:"usage"`
}

type ChatCompletionStreamResponse struct {
	Choices []struct {
		Delta struct {
			Content string `json:"content"`
		} `json:"delta"`
		FinishReason string `json:"finish_reason"`
	} `json:"choices"`
}

// Clean JSON response block generated by LLM (removes markdown code block backticks if present)
func cleanJSONString(s string) string {
	s = strings.TrimSpace(s)
	// Remove markdown code block if present
	if strings.HasPrefix(s, "```") {
		// regex to remove ```json ... ``` or ``` ... ```
		re := regexp.MustCompile("(?s)^```(?:json)?\\n(.*)\\n```$")
		matches := re.FindStringSubmatch(s)
		if len(matches) > 1 {
			s = matches[1]
		} else {
			// Fallback: just strip first and last line
			lines := strings.Split(s, "\n")
			if len(lines) > 2 {
				s = strings.Join(lines[1:len(lines)-1], "\n")
			}
		}
	}
	return strings.TrimSpace(s)
}

func CallChatCompletion(messages []ChatMessage, taskName string, responseFormatJSON bool) (string, error) {
	cfg := config.GetAIConfig(taskName)
	if cfg.BaseURL == "" || cfg.APIKey == "" {
		return "", errors.New("LLM API base URL or API key is not configured")
	}

	url := fmt.Sprintf("%s/chat/completions", strings.TrimRight(cfg.BaseURL, "/"))
	reqBody := ChatCompletionRequest{
		Model:    cfg.Model,
		Messages: messages,
	}

	if responseFormatJSON {
		reqBody.ResponseFormat = map[string]string{"type": "json_object"}
	}
	if cfg.MaxTokens != nil {
		reqBody.MaxTokens = cfg.MaxTokens
	}

	jsonBytes, err := json.Marshal(reqBody)
	if err != nil {
		return "", err
	}

	req, err := http.NewRequest("POST", url, bytes.NewBuffer(jsonBytes))
	if err != nil {
		return "", err
	}

	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("Authorization", fmt.Sprintf("Bearer %s", cfg.APIKey))

	client := &http.Client{Timeout: 120 * time.Second}
	resp, err := client.Do(req)
	if err != nil || (err == nil && resp.StatusCode == http.StatusBadRequest) {
		if responseFormatJSON {
			if resp != nil && resp.Body != nil {
				resp.Body.Close()
			}
			reqBody.ResponseFormat = nil
			jsonBytesRetry, _ := json.Marshal(reqBody)
			reqRetry, errRetryReq := http.NewRequest("POST", url, bytes.NewBuffer(jsonBytesRetry))
			if errRetryReq == nil {
				reqRetry.Header.Set("Content-Type", "application/json")
				reqRetry.Header.Set("Authorization", fmt.Sprintf("Bearer %s", cfg.APIKey))
				respRetry, errRetry := client.Do(reqRetry)
				if errRetry == nil {
					resp = respRetry
					err = nil
				}
			}
		}
	}

	if err != nil {
		return "", err
	}
	defer resp.Body.Close()

	if resp.StatusCode != http.StatusOK {
		bodyBytes, _ := io.ReadAll(resp.Body)
		return "", fmt.Errorf("AI API returned status %d: %s", resp.StatusCode, string(bodyBytes))
	}

	var completionResp ChatCompletionResponse
	if err := json.NewDecoder(resp.Body).Decode(&completionResp); err != nil {
		return "", err
	}

	if completionResp.Usage.TotalTokens > 0 {
		crud.RecordTokenUsage(completionResp.Usage.PromptTokens, completionResp.Usage.CompletionTokens, completionResp.Usage.TotalTokens)
	}

	if len(completionResp.Choices) == 0 {
		return "", errors.New("AI API returned empty choices")
	}

	return completionResp.Choices[0].Message.Content, nil
}

// Helper to call OpenAI API in streaming mode and return connection reader
func CallChatCompletionStream(messages []ChatMessage, taskName string) (*http.Response, error) {
	cfg := config.GetAIConfig(taskName)
	if cfg.BaseURL == "" || cfg.APIKey == "" {
		return nil, errors.New("LLM API base URL or API key is not configured")
	}

	url := fmt.Sprintf("%s/chat/completions", strings.TrimRight(cfg.BaseURL, "/"))
	reqBody := ChatCompletionRequest{
		Model:    cfg.Model,
		Messages: messages,
		Stream:   true,
	}
	if cfg.MaxTokens != nil {
		reqBody.MaxTokens = cfg.MaxTokens
	}

	jsonBytes, err := json.Marshal(reqBody)
	if err != nil {
		return nil, err
	}

	req, err := http.NewRequest("POST", url, bytes.NewBuffer(jsonBytes))
	if err != nil {
		return nil, err
	}

	req.Header.Set("Content-Type", "application/json")
	req.Header.Set("Authorization", fmt.Sprintf("Bearer %s", cfg.APIKey))

	// No timeout set here because it's a stream connection
	client := &http.Client{}
	resp, err := client.Do(req)
	if err != nil {
		return nil, err
	}

	if resp.StatusCode != http.StatusOK {
		bodyBytes, _ := io.ReadAll(resp.Body)
		resp.Body.Close()
		return nil, fmt.Errorf("AI API stream returned status %d: %s", resp.StatusCode, string(bodyBytes))
	}

	return resp, nil
}

