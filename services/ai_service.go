func engagementScore(row map[string]interface{}) float64 {
	score := 0.0

	if bump, ok := row["manual_bump"].(string); ok {
		if bump == "read" {
			score += 5.0
		} else if bump == "glance" {
			score -= 3.0
		}
	}

	if fav, ok := row["favorited"].(int); ok && fav > 0 {
		score += 4.0
	}
	if openOrig, ok := row["opened_original"].(int); ok && openOrig > 0 {
		score += 3.0
	}
	if scrollBot, ok := row["scrolled_to_bottom"].(int); ok && scrollBot > 0 {
		score += 2.0
	}

	if dwell, ok := row["active_dwell_ms"].(int); ok {
		dwellMin := float64(dwell) / 60000.0
		if dwellMin > 2.0 {
			dwellMin = 2.0
		}
		score += dwellMin
	}

	if scrollPct, ok := row["scrolled_pct"].(float64); ok {
		score += scrollPct * 1.5
	} else if scrollPctInt, ok := row["scrolled_pct"].(int); ok {
		score += float64(scrollPctInt) * 1.5
	}

	return score
}

func AggregateUserInterestsSnapshot(engagementList []map[string]interface{}) (*UserInterestSnapshot, error) {
	if len(engagementList) == 0 {
		return nil, errors.New("engagement data list is empty")
	}

	scored := make([]scoredEngagement, len(engagementList))
	for i, r := range engagementList {
		scored[i] = scoredEngagement{
			score: engagementScore(r),
			row:   r,
		}
	}

	sort.Slice(scored, func(i, j int) bool {
		return scored[i].score > scored[j].score
	})

	var high []map[string]interface{}
	var low []map[string]interface{}
	for _, se := range scored {
		if se.score >= 4.0 {
			high = append(high, se.row)
		} else if se.score <= 0.0 {
			low = append(low, se.row)
		}
	}

	highLimit := 80
	if len(high) < highLimit {
		highLimit = len(high)
	}
	lowLimit := 80
	if len(low) < lowLimit {
		lowLimit = len(low)
	}

	var highTitles []string
	for _, r := range high[:highLimit] {
		feedName, _ := r["feed_name"].(string)
		title, _ := r["title"].(string)
		highTitles = append(highTitles, fmt.Sprintf("[%s] %s", feedName, title))
	}

	var lowTitles []string
	for _, r := range low[:lowLimit] {
		feedName, _ := r["feed_name"].(string)
		title, _ := r["title"].(string)
		lowTitles = append(lowTitles, fmt.Sprintf("[%s] %s", feedName, title))
	}

	highText := "（数据不足）"
	if len(highTitles) > 0 {
		var buf bytes.Buffer
		for _, t := range highTitles {
			buf.WriteString("- " + t + "\n")
		}
		highText = buf.String()
	}

	lowText := "（数据不足）"
	if len(lowTitles) > 0 {
		var buf bytes.Buffer
		for _, t := range lowTitles {
			buf.WriteString("- " + t + "\n")
		}
		lowText = buf.String()
	}

	prompt := fmt.Sprintf(`你是一个用户阅读行为分析助手。以下是某用户近30天的RSS阅读记录。

## 用户高度关注的文章（长时间阅读、滚动到底、收藏、查看原文、手动提升注意力）
%s
## 用户较少关注的文章（快速跳过、很少互动）
%s
请分析并输出以下 JSON（不要输出其他内容）：
{
  "high_interest": [
    {"topic": "主题名称（简短，4-10字）", "description": "一句话描述用户在这个主题上的关注点", "strength": "high 或 medium"}
  ],
  "low_interest": [
    {"topic": "主题名称", "description": "一句话描述"}
  ],
  "attention_guide": "一段自然语言，50-120字，概括用户的整体阅读倾向，供分类器参考。格式示例：'用户高度关注XX and XX方向，尤其是涉及XX的内容应标为read；对XX and XX类内容兴趣较低，可标为glance。'",
  "concentration_note": "如果 high_interest 中超过半数主题属于同一领域，输出一句带黑色幽默、毒舌、戏谑又一针见血的调侃，无情揭露用户偏科的信息茧房（30-60字），语气要傲娇或犀利，否则设为 null"
}

要求：
- high_interest 提取 3-8 个主题，按关注强度从高到低排列
- low_interest 提取 2-5 个主题
- 主题应跨订阅源归纳，不要按订阅源罗列
- 如果某类文章高参与和低参与中都有，说明用户对该主题的子方向有选择性，请在 description 中体现
- attention_guide 必须具体到可操作，不要说"用户关注科技"这种空话`, highText, lowText)

	messages := []ChatMessage{
		{Role: "system", Content: "You are a helpful reading behavior analyst."},
		{Role: "user", Content: prompt},
	}

	content, err := CallChatCompletion(messages, "profile", true)
	if err != nil {
		return nil, err
	}

	cleaned := cleanJSONString(content)
	var snapshot UserInterestSnapshot
	if err := json.Unmarshal([]byte(cleaned), &snapshot); err != nil {
		log.Printf("[AI] Failed to parse user interest profile JSON: %s. Raw: %s", err, content)
		return nil, err
	}

	return &snapshot, nil
}

// Estimate character count length in Chinese equivalent
func EstimateCleanTextLength(text string) int {
	// Simple runecount represents character length roughly in UTF-8
	return len([]rune(text))
}

// Stream reader helper for SSE server transmission
func ReadSSEResponse(resp *http.Response, onChunk func(string) error) error {
	reader := bufio.NewReader(resp.Body)
	for {
		line, err := reader.ReadString('\n')
		if err != nil {
			if err == io.EOF {
				break
			}
			return err
		}

		line = strings.TrimSpace(line)
		if line == "" {
			continue
		}

		if strings.HasPrefix(line, "data: ") {
			data := strings.TrimPrefix(line, "data: ")
			if data == "[DONE]" {
				break
			}

			var streamResp ChatCompletionStreamResponse
			if err := json.Unmarshal([]byte(data), &streamResp); err == nil {
				if len(streamResp.Choices) > 0 {
					content := streamResp.Choices[0].Delta.Content
					if content != "" {
						if err := onChunk(content); err != nil {
							return err
						}
					}
					if streamResp.Choices[0].FinishReason == "stop" {
						break
					}
				}
			}
		}
	}
	return nil
}

// --- Translation Services ---

func DetectLanguage(text string) string {
	if text == "" {
		return "en"
	}
	// Regex for Chinese characters: \p{Han}
	re := regexp.MustCompile(`\p{Han}`)
	matches := re.FindAllString(text, -1)
	zhChars := len(matches)
	totalChars := len([]rune(text))
	if totalChars == 0 {
		return "en"
	}
	if float64(zhChars)/float64(totalChars) > 0.05 {
		return "zh"
	}
	return "en"
}

func GenerateTranslation(title string, text string, targetLang string) (string, error) {
	if text == "" {
		return "", nil
	}

	paragraphs := strings.Split(text, "\n")
	var chunks []string
	var currentChunk []string
	currentLen := 0

	for _, p := range paragraphs {
		pLen := len([]rune(p))
		if currentLen+pLen > 800 && len(currentChunk) > 0 {
			chunks = append(chunks, strings.Join(currentChunk, "\n"))
			currentChunk = []string{p}
			currentLen = pLen
		} else {
			currentChunk = append(currentChunk, p)
			currentLen += pLen + 1
		}
	}
	if len(currentChunk) > 0 {
		chunks = append(chunks, strings.Join(currentChunk, "\n"))
	}

	engName, localName, chnName := GetLanguageNames(targetLang)

	systemPrompt := fmt.Sprintf(`You are a professional translator.
Your task is to translate the given text into %s (%s).
Rules:
- Keep the paragraph structure and line breaks EXACTLY identical to the source text.
- Do NOT add any notes, explanations, introduction, or prefix. Output ONLY the translated paragraphs.
- Translate to %s (%s) faithfully, maintaining the original tone and style.
- CRITICAL: Regardless of the source language, you must translate it into %s (%s). Do NOT copy or output the original text if it is in a different language. You MUST output the translation in %s.
- 必须且只能将文本翻译为 %s (%s)，绝对不要直接输出原文！请输出完整的翻译后文本。`, engName, localName, engName, localName, engName, localName, localName, chnName, localName)

	type chunkResult struct {
		index int
		text  string
		err   error
	}

	resultsChan := make(chan chunkResult, len(chunks))
	sem := make(chan struct{}, 3) // Semaphore to limit concurrency

	for i, chunkText := range chunks {
		go func(idx int, cText string) {
			if strings.TrimSpace(cText) == "" {
				resultsChan <- chunkResult{index: idx, text: cText, err: nil}
				return
			}

			sem <- struct{}{}
			defer func() { <-sem }()

			userContent := fmt.Sprintf("Please translate the following text into %s (%s). Do NOT output the original text, only output the translated text:\n\n%s", engName, localName, cText)
			if chnName != "英文" {
				userContent = fmt.Sprintf("请将以下文本翻译为%s (%s)。请务必翻译，不要输出原文，只输出翻译后的文本：\n\n%s", chnName, localName, cText)
			}

			messages := []ChatMessage{
				{Role: "system", Content: systemPrompt},
				{Role: "user", Content: userContent},
			}

			translated, err := CallChatCompletion(messages, "summary", false)
			if err != nil {
				resultsChan <- chunkResult{index: idx, text: cText, err: err}
			} else {
				resultsChan <- chunkResult{index: idx, text: translated, err: nil}
			}
		}(i, chunkText)
	}

	translatedChunks := make([]string, len(chunks))
	for k := 0; k < len(chunks); k++ {
		res := <-resultsChan
		translatedChunks[res.index] = res.text
	}

	return strings.Join(translatedChunks, "\n"), nil
}

func GetEntryTranslation(entryID int, force bool) (map[string]interface{}, error) {
	entry, err := crud.GetEntryByID(entryID)
	if err != nil {
		return nil, err
	}
	if entry == nil {
		return nil, errors.New("entry not found")
	}

	targetLang := config.GlobalConfig.AI.SummaryLanguage
	if targetLang == "auto" || targetLang == "" {
		targetLang = config.GlobalConfig.SystemLanguage
	}
	if targetLang == "" {
		targetLang = "zh"
	}

	if force {
		_ = crud.DeleteTranslation(entryID)
		_ = crud.DeleteParagraphTranslations(entryID)
	} else {
		transRow, _ := crud.GetTranslation(entryID, targetLang)
		if transRow != nil {
			return map[string]interface{}{
				"translated_content": transRow.Content,
				"target_lang":        transRow.Lang,
				"status":             "ok",
			}, nil
		}
	}

	ftRow, _ := crud.GetEntryFulltext(entryID)
	var ftContent string
	if ftRow != nil {
		ftContent = ftRow.Content
	} else {
		ftContent = crud.CleanHTML(entry.RawContent)
	}

	if ftContent == "" || len([]rune(ftContent)) < 5 {
		return nil, errors.New("no content to translate")
	}

	sourceLang := DetectLanguage(ftContent)
	isSourceZh := (sourceLang == "zh" || sourceLang == "zh-hant")
	isTargetZh := (targetLang == "zh" || targetLang == "zh-hant")

	if sourceLang == targetLang || (isSourceZh && isTargetZh) {
		_ = crud.SaveTranslation(entryID, ftContent, targetLang)
		return map[string]interface{}{
			"translated_content": ftContent,
			"target_lang":        targetLang,
			"status":             "ok",
		}, nil
	}

	translated, err := GenerateTranslation(entry.Title, ftContent, targetLang)
	if err != nil {
		return nil, err
	}

	_ = crud.SaveTranslation(entryID, translated, targetLang)

	return map[string]interface{}{
		"translated_content": translated,
		"target_lang":        targetLang,
		"status":             "ok",
	}, nil
}

func TranslateEntryParagraph(entryID int, paraIndex int, text string) (map[string]interface{}, error) {
	entry, err := crud.GetEntryByID(entryID)
	if err != nil {
		return nil, err
	}
	if entry == nil {
		return nil, errors.New("entry not found")
	}

	targetLang := config.GlobalConfig.AI.SummaryLanguage
	if targetLang == "auto" || targetLang == "" {
		targetLang = config.GlobalConfig.SystemLanguage
	}
	if targetLang == "" {
		targetLang = "zh"
	}

	textToTranslate := strings.TrimSpace(text)
	if textToTranslate == "" {
		return map[string]interface{}{
			"translated_text": "",
			"status":          "ok",
		}, nil
	}

	cached, _ := crud.GetParagraphTranslation(entryID, paraIndex, targetLang)
	if cached != nil && cached.OriginalText == textToTranslate {
		return map[string]interface{}{
			"translated_text": cached.TranslatedText,
			"status":          "ok",
		}, nil
	}

	sourceLang := DetectLanguage(textToTranslate)
	isSourceZh := (sourceLang == "zh" || sourceLang == "zh-hant")
	isTargetZh := (targetLang == "zh" || targetLang == "zh-hant")

	translatedText := textToTranslate
	if !(sourceLang == targetLang || (isSourceZh && isTargetZh)) {
		translated, err := GenerateTranslation(entry.Title, textToTranslate, targetLang)
		if err == nil {
			translatedText = translated
		}
	}

	_ = crud.SaveParagraphTranslation(entryID, paraIndex, targetLang, textToTranslate, translatedText)

	return map[string]interface{}{
		"translated_text": translatedText,
		"status":          "ok",
	}, nil
}

func GetLanguageNames(langCode string) (string, string, string) {
	langMap := map[string][3]string{
		"zh":      {"Simplified Chinese (简体中文)", "简体中文", "简体中文"},
		"zh-hant": {"Traditional Chinese (繁体中文)", "繁體中文", "繁体中文"},
		"en":      {"English", "English", "英文"},
		"ja":      {"Japanese (日本語)", "日本語", "日语"},
		"ko":      {"Korean (한국어)", "한국어", "韩语"},
		"fr":      {"French (Français)", "Français", "法语"},
		"es":      {"Spanish (Español)", "Español", "西班牙语"},
		"de":      {"German (Deutsch)", "Deutsch", "德语"},
		"ru":      {"Russian (Русский)", "Русский", "俄语"},
		"pt":      {"Portuguese (Português)", "Português", "葡萄牙语"},
		"it":      {"Italian (Italiano)", "Italiano", "意大利语"},
	}
	if val, ok := langMap[langCode]; ok {
		return val[0], val[1], val[2]
	}
	return "Simplified Chinese (简体中文)", "简体中文", "简体中文" // default fallback
}
